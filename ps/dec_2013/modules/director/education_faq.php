<b>Q5. What are Program Educational Objectives(PEOs)?</b>
<br/>


Broad statements that describe what graduates are expected to attain within a few years of graduation. 
Program educational objectives are based on the needs of the program's constituencies.
Program educational objectives are broad statements that describe the career and professional accomplishments 
that the program is preparing the graduates to achieve.

<br/>
<b>Q6. What are Program Outcomes (POs)?</b>
<br/>


Statements that describe what students are expected to know and be able to do by the time of graduation. 
These relate to skills, knowledge, and behaviors that students acquire as they progress through the program.

<br/>
<b>Q7. What are Course Outcomes (COs)?</b>
<br/>


Course outcomes are statements of what a student should know, understand and/or be able to demonstrate after completion of a  course.

