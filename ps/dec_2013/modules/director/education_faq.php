<b>Q4. Describe Graduate Attributes (GA) defined by NBA.</b>
<br/>


The Graduate Attributes of Engineering Programs as identified by NBA (January 2013) are: 
<br/>1. Engineering knowledge: Apply the knowledge of mathematics, science, engineering 
fundamentals, and an engineering specialization to the solution of complex engineering 
problems. 
<br/>2. Problem analysis: Identify, formulate, research literature, and analyze complex engineering 
problems reaching substantiated conclusions using first principles of mathematics, natural 
sciences, and engineering sciences. 
<br/>3. Design/development of solutions: Design solutions for complex engineering problems and 
design system components or processes that meet the specified needs with appropriate 
consideration for the public health and safety, and the cultural, societal, and environmental 
considerations. 
<br/>4. Conduct investigations of complex problems: Use research-based knowledge and 
research methods including design of experiments, analysis and interpretation of data, and 
synthesis of the information to provide valid conclusions. 
<br/>5. Modern tool usage: Create, select, and apply appropriate techniques, resources, and modern 
engineering and IT tools including prediction and modeling to complex engineering activities 
with an understanding of the limitations. 
<br/>6. The engineer and society: Apply reasoning informed by the contextual knowledge to assess 
societal, health, safety, legal and cultural issues and the consequent responsibilities relevant 
to the professional engineering practice. 
<br/>7. Environment and sustainability: Understand the impact of the professional engineering 
solutions in societal and environmental contexts, and demonstrate the knowledge of, and need 
for sustainable development. 
<br/>8. Ethics: Apply ethical principles and commit to professional ethics and responsibilities and 
norms of the engineering practice. 
<br/>9. Individual and team work: Function effectively as an individual, and as a member or leader 
in diverse teams, and in multidisciplinary settings. 
<br/>10. Communication: Communicate effectively on complex engineering activities with the 
engineering community and with society at large, such as, being able to comprehend and write 
effective reports and design documentation, make effective presentations, and give and 
receive clear instructions. 
<br/>11. Project management and finance: Demonstrate knowledge and understanding of the 
engineering and management principles and apply these to one’s own work, as a member and 
leader in a team, to manage projects and in multidisciplinary environments. 
<br/>12. Life-long learning: Recognize the need for, and have the preparation and ability to engage in 
independent and life-long learning in the broadest context of technological change. 

