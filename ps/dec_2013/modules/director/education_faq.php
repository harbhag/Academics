<b>Q1. What is an Accord?</b>
<br/>
An agreement among the bodies responsible for accrediting specific degree programs in each of the signatory countries. 
Accords recognize the substantial equivalency of programs accredited by each of these bodies and 
recommends that graduates of accredited programs in any of the signatory countries be recognized by the other 
countries as having met the academic requirements for entry-level practice. 
Accords are intended to improve technical education worldwide and foster the mobility of students and graduates.

