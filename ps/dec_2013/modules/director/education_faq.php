<b>Q8. What is Assessment?</b>
<br/>


Assessment tells us what our students are learning and how well they are learning that material. 
Assessment is an ongoing process in which faculty and administrators determine what knowledge and skills students should be learning. 
It is one or more processes that identify, collect, and prepare data to evaluate the achievement of course outcomes, program outcomes 
and program educational objectives.

