<b>Q16. Describe Revised Bloom's Taxonomy or Anderson and Krathwohl's Taxonomy ?</b>
<br/>


Anderson and Krathwohl (2001) revised Bloom's Taxonomy.
The revised Bloom taxonomy gives slightly different names to the 6 levels of the hierarchy:
remember (was knowledge)
understand (was comprehension)
apply (was application)
analyze (was analysis)
evaluate (was evaluation)
create (was synthesis)

Different levels of thinking are described as:

<br/>1. Remember – recalling relevant terminology, specific facts, or different procedures related to information and/or course topics. At this level, a student can remember something, but may not really understand it. 

<br/>2. Understand – the ability to grasp the meaning of information (facts, definitions, concepts, etc.) that has been presented. 

<br/>3. Apply – being able to use previously learned information in different situations or in problem solving. 

<br/>4. Analyze – the ability to break information down into its component parts. Analysis also refers to the process of examining information in order to make conclusions regarding cause and effect, interpreting motives, making inferences, or finding evidence to support statements/arguments. 

<br/>5. Evaluate – being able to judge the value of information and/or sources of information based on personal values or opinions. 

<br/>6. Create – the ability to creatively or uniquely apply prior knowledge and/or skills to produce new and original thoughts, ideas, processes, etc. At this level, students are involved in creating their own thoughts and ideas. 

