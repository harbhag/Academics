<b>Q9. What is Evaluation? </b>
<br/>


It is one or more processes for interpreting the data and evidence accumulated through assessment practices. 
Evaluation determines the extent to which program outcomes or program educational objectives are 
being achieved and results in decisions and actions to improve the program.

