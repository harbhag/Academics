<b>Q12. Describe rubrics.</b>
<br/>


Rubrics are a way to set up custom or Outcome-based assessment criteria for scoring.
A Rubric is an assessment tool for communicating expectations of quality. Rubrics are typically comprised of rows and columns. Rows are used to define the various criteria being used to assess an assignment. Columns are used to define levels of performance for each criterion.
Teachers have found rubrics to be every useful in providing guidance and feedback to students where skills and processes are the targets being monitored.
Rubrics enable teachers to evaluate students' performance in situations that more closely replicate real life than an isolated test. Rubrics also help teachers to focus their own attention to the key concepts and standards that the students must obtain.
Rubrics provide students with a clear understanding of what is expected of them. Students have concrete directions about what makes a good science project, a good persuasive writing piece, etc.. Many studies have proven that rubrics improve students' end products and thus increase the students' overall learning. Rubrics provide students with valuable information about the degree of which a specific learning outcome has been achieved. They provide students with concrete feedback that displays areas of strength and areas in need of improvement. 
Students can use this feedback as a tool to further develop their abilities.
Rubrics encourage students to think about their own thinking and possibly about their own criteria for what is "good.": to analyze their own work and process to see how it matches up with the standard explained in the rubric. By using rubrics, teachers give students experience in their higher-level thinking processes.
Parents appreciate the use of rubrics because they allow teachers to justify why a certain grade was assigned to his/her child.

